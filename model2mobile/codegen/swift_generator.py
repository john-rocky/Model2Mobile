def _build_output_parsing_body(outputs: list[dict[str, Any]]) -> str:
    """Generate the body of parseOutput() for the predictor."""
    lines: list[str] = []
    lines.append("        var results: [String: MLMultiArray] = [:]")
    for out in outputs:
        name = out["name"]
        lines.append(f'        guard let {_swift_var(name)} = output.featureValue(for: "{name}")?.multiArrayValue else {{')
        lines.append(f'            throw PredictorError.outputMissing("{name}")')
        lines.append("        }")
        lines.append(f'        results["{name}"] = {_swift_var(name)}')
    lines.append("        return results")
    return "\n".join(lines)


def _swift_var(name: str) -> str:
    """Convert an output name to a valid Swift variable name (camelCase)."""
    cleaned = re.sub(r"[^a-zA-Z0-9]", "_", name)
    parts = [p for p in cleaned.split("_") if p]
    if not parts:
        return "output"
    result = parts[0].lower()
    for p in parts[1:]:
        result += p[0].upper() + p[1:]
    return result


def _build_output_name_constants(outputs: list[dict[str, Any]]) -> str:
    """Generate static let constants for output tensor names."""
    lines: list[str] = []
    for out in outputs:
        name = out["name"]
        var = _swift_var(name)
        lines.append(f'    static let outputName{var[0].upper() + var[1:]} = "{name}"')
    return "\n".join(lines)


def _build_output_struct_fields(outputs: list[dict[str, Any]]) -> str:
    """Generate struct fields for the simple output parser."""
    lines: list[str] = []
    for out in outputs:
        name = out["name"]
        var = _swift_var(name)
        shape_comment = ""
        if "shape" in out and out["shape"]:
            shape_comment = f"  // shape: {out['shape']}"
        lines.append(f"    let {var}: MLMultiArray?{shape_comment}")
    return "\n".join(lines)


def _build_output_struct_init_args(outputs: list[dict[str, Any]]) -> str:
    """Generate init arguments for the simple output struct."""
    lines: list[str] = []
    for out in outputs:
        name = out["name"]
        var = _swift_var(name)
        lines.append(f'            {var}: outputs["{name}"],')
    return "\n".join(lines)


def generate_swift_code(
    coreml_path: str,
    model_name: str,
    recipe_results: list | None = None,
) -> dict[str, str]:
    """Generate Swift code files for a Core ML model.

    Reads the model spec to understand inputs/outputs, then produces
    ready-to-use Swift source files.

    Args:
        coreml_path: Path to the .mlpackage directory.
        model_name: Human-readable model name (used for class naming).
        recipe_results: List of RecipeResult objects from conversion (to detect NMS stripping).

    Returns:
        Dictionary mapping filename to Swift source code content.
    """
    spec = _load_spec(coreml_path)
    description = spec.description

    # Analyze inputs
    inputs = [_describe_input(inp) for inp in description.input]
    outputs = [_describe_output(out) for out in description.output]

    if not inputs:
        logger.warning("Model has no inputs; skipping code generation")
        return {}

    class_name = _sanitize_swift_name(model_name)
    raw_model_name = Path(coreml_path).stem  # filename without extension

    # Determine primary input type
    primary_input = inputs[0]
    is_image_input = primary_input["kind"] == "image"

    # Build output parsing body (shared between templates)
    output_return_type = "[String: MLMultiArray]"
    output_body = _build_output_parsing_body(outputs)

    files: dict[str, str] = {}

    # --- Generate Predictor.swift ---
    if is_image_input:
        predictor_code = _PREDICTOR_IMAGE_TEMPLATE.format(
            class_name=class_name,
            model_name=raw_model_name,
            input_name=primary_input["name"],
            input_width=primary_input.get("width", 640),
            input_height=primary_input.get("height", 640),
            output_return_type=output_return_type,
            output_parsing_body=output_body,
        )
    else:
        # MultiArray input
        shape = primary_input.get("shape", [1, 3, 640, 640])
        shape_literal = "[" + ", ".join(str(s) for s in shape) + "]"
        shape_comment = "[" + ", ".join(str(s) for s in shape) + "]"

        predictor_code = _PREDICTOR_MULTIARRAY_TEMPLATE.format(
            class_name=class_name,
            model_name=raw_model_name,
            input_name=primary_input["name"],
            input_shape_literal=shape_literal,
            input_shape_comment=shape_comment,
            output_return_type=output_return_type,
            output_parsing_body=output_body,
        )

    files[f"{class_name}Predictor.swift"] = predictor_code

    # --- Generate PostProcessor.swift ---
    nms_stripped = _has_nms_stripped(recipe_results)
    output_name_constants = _build_output_name_constants(outputs)

    if nms_stripped:
        postprocessor_code = _POSTPROCESSOR_NMS_TEMPLATE.format(
            class_name=class_name,
            output_name_constants=output_name_constants,
        )
    else:
        output_struct_fields = _build_output_struct_fields(outputs)
        output_struct_init_args = _build_output_struct_init_args(outputs)

        postprocessor_code = _POSTPROCESSOR_SIMPLE_TEMPLATE.format(
            class_name=class_name,
            output_name_constants=output_name_constants,
            output_struct_fields=output_struct_fields,
            output_struct_init_args=output_struct_init_args,
        )

    files[f"{class_name}PostProcessor.swift"] = postprocessor_code

    return files


def save_swift_code(
    coreml_path: str,
    model_name: str,
    output_dir: Path,
    recipe_results: list | None = None,
) -> list[Path]:
    """Generate and save Swift files to output_dir.

    Args:
        coreml_path: Path to the .mlpackage directory.
        model_name: Human-readable model name (used for class naming).
        output_dir: Directory to write generated Swift files into.
        recipe_results: List of RecipeResult objects from conversion.

    Returns:
        List of Path objects for each generated file.
    """
    files = generate_swift_code(coreml_path, model_name, recipe_results)

    if not files:
        logger.warning("No Swift code generated")
        return []

    output_dir.mkdir(parents=True, exist_ok=True)
    saved: list[Path] = []

    for filename, content in files.items():
        path = output_dir / filename
        path.write_text(content, encoding="utf-8")
        saved.append(path)
        logger.info("Generated Swift file: %s", path)

    return saved

def _build_output_parsing_body(outputs: list[dict[str, Any]]) -> str:
    """Generate the body of parseOutput() for the predictor."""
    lines: list[str] = []
    lines.append("        var results: [String: MLMultiArray] = [:]")
    for out in outputs:
        name = out["name"]
        lines.append(f'        guard let {_swift_var(name)} = output.featureValue(for: "{name}")?.multiArrayValue else {{')
        lines.append(f'            throw PredictorError.outputMissing("{name}")')
        lines.append("        }")
        lines.append(f'        results["{name}"] = {_swift_var(name)}')
    lines.append("        return results")
    return "\n".join(lines)


def _swift_var(name: str) -> str:
    """Convert an output name to a valid Swift variable name (camelCase)."""
    cleaned = re.sub(r"[^a-zA-Z0-9]", "_", name)
    parts = [p for p in cleaned.split("_") if p]
    if not parts:
        return "output"
    result = parts[0].lower()
    for p in parts[1:]:
        result += p[0].upper() + p[1:]
    return result


def _build_output_name_constants(outputs: list[dict[str, Any]]) -> str:
    """Generate static let constants for output tensor names."""
    lines: list[str] = []
    for out in outputs:
        name = out["name"]
        var = _swift_var(name)
        lines.append(f'    static let outputName{var[0].upper() + var[1:]} = "{name}"')
    return "\n".join(lines)


def _build_output_struct_fields(outputs: list[dict[str, Any]]) -> str:
    """Generate struct fields for the simple output parser."""
    lines: list[str] = []
    for out in outputs:
        name = out["name"]
        var = _swift_var(name)
        shape_comment = ""
        if "shape" in out and out["shape"]:
            shape_comment = f"  // shape: {out['shape']}"
        lines.append(f"    let {var}: MLMultiArray?{shape_comment}")
    return "\n".join(lines)


def _build_output_struct_init_args(outputs: list[dict[str, Any]]) -> str:
    """Generate init arguments for the simple output struct."""
    lines: list[str] = []
    for out in outputs:
        name = out["name"]
        var = _swift_var(name)
        lines.append(f'            {var}: outputs["{name}"],')
    return "\n".join(lines)



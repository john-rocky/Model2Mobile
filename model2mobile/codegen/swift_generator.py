"""Swift inference code generation for Core ML models.

Reads the Core ML model spec to understand inputs/outputs, then generates
production-ready Swift files that users can drop into an Xcode project.
"""

from __future__ import annotations

import logging
import re
from pathlib import Path
from typing import Any

import coremltools as ct

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Model spec introspection
# ---------------------------------------------------------------------------


def _sanitize_swift_name(name: str) -> str:
    """Convert a raw model name into a valid Swift identifier (PascalCase)."""
    # Strip extension and path
    name = Path(name).stem
    # Replace non-alphanumeric chars with underscores
    name = re.sub(r"[^a-zA-Z0-9]", "_", name)
    # PascalCase: split on underscores and capitalize each word
    parts = [p for p in name.split("_") if p]
    if not parts:
        return "Model"
    return "".join(p[0].upper() + p[1:] for p in parts)


def _load_spec(coreml_path: str) -> Any:
    """Load the Core ML model spec without loading weights."""
    model = ct.models.MLModel(coreml_path, skip_model_load=True)
    return model.get_spec()


def _describe_input(feature: Any) -> dict[str, Any]:
    """Extract input metadata from a feature description proto."""
    info: dict[str, Any] = {"name": feature.name}
    ftype = feature.type

    if ftype.HasField("imageType"):
        img = ftype.imageType
        info["kind"] = "image"
        info["width"] = img.width
        info["height"] = img.height
        # colorSpace: 10=RGB, 20=BGR, 30=GRAYSCALE
        info["channels"] = 1 if img.colorSpace == 30 else 3
    elif ftype.HasField("multiArrayType"):
        ma = ftype.multiArrayType
        info["kind"] = "multiarray"
        info["shape"] = list(ma.shape)
        # dataType: 65568=FLOAT32, 65552=FLOAT16, 131104=INT32
        info["dtype"] = "Float32"
    else:
        info["kind"] = "unknown"
    return info


def _describe_output(feature: Any) -> dict[str, Any]:
    """Extract output metadata from a feature description proto."""
    info: dict[str, Any] = {"name": feature.name}
    ftype = feature.type

    if ftype.HasField("multiArrayType"):
        ma = ftype.multiArrayType
        info["kind"] = "multiarray"
        info["shape"] = list(ma.shape)
    elif ftype.HasField("imageType"):
        info["kind"] = "image"
    elif ftype.HasField("dictionaryType"):
        info["kind"] = "dictionary"
    else:
        info["kind"] = "unknown"
    return info


def _has_nms_stripped(recipe_results: list[Any] | None) -> bool:
    """Check if NMS was stripped during conversion."""
    if not recipe_results:
        return False
    for r in recipe_results:
        name = getattr(r, "recipe_name", "") if hasattr(r, "recipe_name") else str(r)
        if "nms" in name.lower() or "detection_unwrap" in name.lower():
            return True
    return False



_POSTPROCESSOR_DEPTH_TEMPLATE = """\
import CoreML
import UIKit

/// Auto-generated by Model2Mobile
///
/// Post-processing for monocular depth estimation models.
/// Takes raw model output (N, 1, H, W) and produces a normalized depth map.
///
/// Usage:
///   let predictor = try {class_name}Predictor()
///   let raw = try predictor.predict(image: uiImage)
///   let result = {class_name}PostProcessor.depth(outputs: raw)
///   let depthImage = result.depthImage()

// MARK: - Depth result

/// Depth estimation output containing a per-pixel depth map.
struct DepthResult {{
    /// 2D array of depth values normalized to [0, 1], accessed as depthMap[y][x].
    let depthMap: [[Float]]
    /// Width of the depth map in pixels.
    let width: Int
    /// Height of the depth map in pixels.
    let height: Int
    /// Minimum raw depth value before normalization.
    let minDepth: Float
    /// Maximum raw depth value before normalization.
    let maxDepth: Float

    /// Render the depth map as a grayscale UIImage.
    ///
    /// Closer pixels are brighter (white), farther pixels are darker (black).
    ///
    /// - Returns: A grayscale UIImage, or nil on failure.
    func depthImage() -> UIImage? {{
        let bytesPerPixel = 4
        let bytesPerRow = bytesPerPixel * width
        var pixelData = [UInt8](repeating: 255, count: height * bytesPerRow)

        for y in 0..<height {{
            for x in 0..<width {{
                // Invert so near = bright, far = dark (common convention)
                let value = UInt8(clamping: Int((1.0 - depthMap[y][x]) * 255.0))
                let offset = y * bytesPerRow + x * bytesPerPixel
                pixelData[offset + 0] = value  // R
                pixelData[offset + 1] = value  // G
                pixelData[offset + 2] = value  // B
                pixelData[offset + 3] = 255    // A
            }}
        }}

        guard let provider = CGDataProvider(data: Data(pixelData) as CFData),
              let cgImage = CGImage(
                  width: width,
                  height: height,
                  bitsPerComponent: 8,
                  bitsPerPixel: 32,
                  bytesPerRow: bytesPerRow,
                  space: CGColorSpaceCreateDeviceRGB(),
                  bitmapInfo: CGBitmapInfo(rawValue: CGImageAlphaInfo.premultipliedLast.rawValue),
                  provider: provider,
                  decode: nil,
                  shouldInterpolate: false,
                  intent: .defaultIntent
              ) else {{
            return nil
        }}

        return UIImage(cgImage: cgImage)
    }}
}}

// MARK: - Post-processor

enum {class_name}PostProcessor {{

    /// Output tensor names produced by the model.
{output_name_constants}

    // MARK: - Public API

    /// Process raw model outputs into a normalized depth map.
    ///
    /// Expects the primary output tensor to have shape [N, 1, H, W], [1, H, W],
    /// or [H, W]. Normalizes depth values to [0, 1].
    ///
    /// - Parameter outputs: Dictionary of output name -> MLMultiArray from the predictor.
    /// - Returns: A `DepthResult` containing the normalized depth map and metadata.
    static func depth(outputs: [String: MLMultiArray]) -> DepthResult {{
        guard let tensor = outputs.values.first else {{
            return DepthResult(depthMap: [], width: 0, height: 0, minDepth: 0, maxDepth: 0)
        }}

        let shape = (0..<tensor.shape.count).map {{ tensor.shape[$0].intValue }}

        // Determine H, W from the shape.
        let height: Int
        let width: Int

        switch shape.count {{
        case 4:
            // [N, 1, H, W]
            height = shape[2]
            width = shape[3]
        case 3:
            // [1, H, W] or [C, H, W]
            height = shape[1]
            width = shape[2]
        case 2:
            // [H, W]
            height = shape[0]
            width = shape[1]
        default:
            return DepthResult(depthMap: [], width: 0, height: 0, minDepth: 0, maxDepth: 0)
        }}

        let ptr = tensor.dataPointer.assumingMemoryBound(to: Float.self)
        let totalPixels = height * width

        // First pass: find min and max depth values
        var minVal: Float = .greatestFiniteMagnitude
        var maxVal: Float = -.greatestFiniteMagnitude
        for i in 0..<totalPixels {{
            let v = ptr[i]
            if v < minVal {{ minVal = v }}
            if v > maxVal {{ maxVal = v }}
        }}

        let range = maxVal - minVal
        let safeRange = range > .ulpOfOne ? range : 1.0

        // Second pass: normalize to [0, 1] and build 2D array
        var depthMap = [[Float]](repeating: [Float](repeating: 0, count: width), count: height)

        for y in 0..<height {{
            for x in 0..<width {{
                let rawValue = ptr[y * width + x]
                depthMap[y][x] = (rawValue - minVal) / safeRange
            }}
        }}

        return DepthResult(
            depthMap: depthMap,
            width: width,
            height: height,
            minDepth: minVal,
            maxDepth: maxVal
        )
    }}
}}
"""



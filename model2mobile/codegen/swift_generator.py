_POSTPROCESSOR_NMS_TEMPLATE = """\
import CoreML
import UIKit

/// Auto-generated by Model2Mobile
///
/// Post-processing for detection models where NMS was stripped during conversion.
/// Takes raw model output tensors and returns filtered detections.
///
/// Usage:
///   let predictor = try {class_name}Predictor()
///   let raw = try predictor.predict(image: uiImage)
///   let detections = {class_name}PostProcessor.process(
///       outputs: raw,
///       confidenceThreshold: 0.25,
///       iouThreshold: 0.45
///   )

// MARK: - Detection result

/// A single detected object with bounding box, class, and confidence.
struct Detection {{
    /// Bounding box in normalized coordinates [0, 1] relative to input image.
    let bbox: CGRect
    /// Detected class index.
    let classIndex: Int
    /// Confidence score for this detection.
    let confidence: Float

    /// Center-x in normalized coordinates.
    var cx: CGFloat {{ bbox.midX }}
    /// Center-y in normalized coordinates.
    var cy: CGFloat {{ bbox.midY }}
}}

// MARK: - Post-processor

enum {class_name}PostProcessor {{

    /// Output tensor names produced by the model.
{output_name_constants}

    // MARK: - Public API

    /// Decode raw model outputs into filtered detections.
    ///
    /// - Parameters:
    ///   - outputs: Dictionary of output name -> MLMultiArray from the predictor.
    ///   - confidenceThreshold: Minimum confidence to keep a detection (default 0.25).
    ///   - iouThreshold: IoU threshold for NMS merging (default 0.45).
    ///   - maxDetections: Maximum number of detections to return (default 100).
    /// - Returns: An array of `Detection` objects sorted by confidence (descending).
    static func process(
        outputs: [String: MLMultiArray],
        confidenceThreshold: Float = 0.25,
        iouThreshold: Float = 0.45,
        maxDetections: Int = 100
    ) -> [Detection] {{
        // Attempt to find the primary output tensor.
        // Detection models typically output a tensor of shape [1, N, C] or [1, C, N]
        // where N = number of candidate boxes, C = 4 (bbox) + num_classes.
        guard let tensor = outputs.values.first else {{
            return []
        }}

        let shape = (0..<tensor.shape.count).map {{ tensor.shape[$0].intValue }}
        guard shape.count >= 2 else {{ return [] }}

        // Determine layout
        let candidates: [Detection]
        if shape.count == 3 {{
            // [batch, candidates, attrs] or [batch, attrs, candidates]
            let dim1 = shape[1]
            let dim2 = shape[2]
            if dim2 > dim1 {{
                // [1, attrs, candidates] — transpose layout (e.g. YOLOv8)
                candidates = decodeTransposed(tensor: tensor, attrs: dim1, numCandidates: dim2, threshold: confidenceThreshold)
            }} else {{
                // [1, candidates, attrs] — standard layout
                candidates = decodeStandard(tensor: tensor, numCandidates: dim1, attrs: dim2, threshold: confidenceThreshold)
            }}
        }} else if shape.count == 2 {{
            // [candidates, attrs]
            candidates = decodeStandard2D(tensor: tensor, numCandidates: shape[0], attrs: shape[1], threshold: confidenceThreshold)
        }} else {{
            return []
        }}

        // Apply Non-Maximum Suppression
        let nmsResults = nonMaxSuppression(detections: candidates, iouThreshold: iouThreshold)

        // Limit to maxDetections, already sorted by confidence
        return Array(nmsResults.prefix(maxDetections))
    }}

    // MARK: - Decoding helpers

    /// Decode [1, candidates, attrs] layout.
    /// attrs = [cx, cy, w, h, class_scores...]
    private static func decodeStandard(
        tensor: MLMultiArray,
        numCandidates: Int,
        attrs: Int,
        threshold: Float
    ) -> [Detection] {{
        let numClasses = attrs - 4
        guard numClasses > 0 else {{ return [] }}

        var results: [Detection] = []
        let ptr = tensor.dataPointer.assumingMemoryBound(to: Float.self)

        for i in 0..<numCandidates {{
            let base = i * attrs

            // Find best class
            var bestClass = 0
            var bestScore: Float = -1
            for c in 0..<numClasses {{
                let score = ptr[base + 4 + c]
                if score > bestScore {{
                    bestScore = score
                    bestClass = c
                }}
            }}

            guard bestScore >= threshold else {{ continue }}

            // Decode bbox: [cx, cy, w, h] -> CGRect
            let cx = CGFloat(ptr[base + 0])
            let cy = CGFloat(ptr[base + 1])
            let w  = CGFloat(ptr[base + 2])
            let h  = CGFloat(ptr[base + 3])
            let rect = CGRect(x: cx - w / 2, y: cy - h / 2, width: w, height: h)

            results.append(Detection(bbox: rect, classIndex: bestClass, confidence: bestScore))
        }}

        return results
    }}

    /// Decode [1, attrs, candidates] layout (transposed, e.g. YOLOv8).
    /// Row 0..3 = cx, cy, w, h; rows 4.. = class scores.
    private static func decodeTransposed(
        tensor: MLMultiArray,
        attrs: Int,
        numCandidates: Int,
        threshold: Float
    ) -> [Detection] {{
        let numClasses = attrs - 4
        guard numClasses > 0 else {{ return [] }}

        var results: [Detection] = []
        let ptr = tensor.dataPointer.assumingMemoryBound(to: Float.self)

        for i in 0..<numCandidates {{
            // In transposed layout, element [row][col] = ptr[row * numCandidates + col]
            var bestClass = 0
            var bestScore: Float = -1
            for c in 0..<numClasses {{
                let score = ptr[(4 + c) * numCandidates + i]
                if score > bestScore {{
                    bestScore = score
                    bestClass = c
                }}
            }}

            guard bestScore >= threshold else {{ continue }}

            let cx = CGFloat(ptr[0 * numCandidates + i])
            let cy = CGFloat(ptr[1 * numCandidates + i])
            let w  = CGFloat(ptr[2 * numCandidates + i])
            let h  = CGFloat(ptr[3 * numCandidates + i])
            let rect = CGRect(x: cx - w / 2, y: cy - h / 2, width: w, height: h)

            results.append(Detection(bbox: rect, classIndex: bestClass, confidence: bestScore))
        }}

        return results
    }}

    /// Decode 2D [candidates, attrs] layout (no batch dimension).
    private static func decodeStandard2D(
        tensor: MLMultiArray,
        numCandidates: Int,
        attrs: Int,
        threshold: Float
    ) -> [Detection] {{
        // Reuse the standard decoder — memory layout is equivalent
        return decodeStandard(tensor: tensor, numCandidates: numCandidates, attrs: attrs, threshold: threshold)
    }}

    // MARK: - Non-Maximum Suppression

    /// Greedy NMS: keep highest-confidence detection and remove overlapping boxes.
    private static func nonMaxSuppression(
        detections: [Detection],
        iouThreshold: Float
    ) -> [Detection] {{
        // Sort by confidence descending
        let sorted = detections.sorted {{ $0.confidence > $1.confidence }}
        var kept: [Detection] = []
        var suppressed = Set<Int>()

        for i in 0..<sorted.count {{
            guard !suppressed.contains(i) else {{ continue }}
            let a = sorted[i]
            kept.append(a)

            for j in (i + 1)..<sorted.count {{
                guard !suppressed.contains(j) else {{ continue }}
                let b = sorted[j]
                if iou(a.bbox, b.bbox) > CGFloat(iouThreshold) {{
                    suppressed.insert(j)
                }}
            }}
        }}

        return kept
    }}

    /// Compute Intersection-over-Union for two CGRects.
    private static func iou(_ a: CGRect, _ b: CGRect) -> CGFloat {{
        let intersection = a.intersection(b)
        guard !intersection.isNull else {{ return 0 }}
        let intersectionArea = intersection.width * intersection.height
        let unionArea = a.width * a.height + b.width * b.height - intersectionArea
        guard unionArea > 0 else {{ return 0 }}
        return intersectionArea / unionArea
    }}
}}
"""



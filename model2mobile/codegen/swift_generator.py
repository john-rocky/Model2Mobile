_POSTPROCESSOR_CLASSIFICATION_TEMPLATE = """\
import CoreML
import UIKit

/// Auto-generated by Model2Mobile
///
/// Post-processing for classification models.
/// Takes raw model output logits or probabilities and returns top-K predictions.
///
/// Usage:
///   let predictor = try {class_name}Predictor()
///   let raw = try predictor.predict(image: uiImage)
///   let results = {class_name}PostProcessor.classify(
///       outputs: raw,
///       topK: 5,
///       labels: ["cat", "dog", ...]  // optional
///   )

// MARK: - Classification result

/// A single classification prediction with class index, confidence, and optional label.
struct ClassificationResult {{
    /// Index of the predicted class in the output tensor.
    let classIndex: Int
    /// Confidence score (probability) for this class.
    let confidence: Float
    /// Human-readable label, if a label list was provided.
    let label: String?
}}

// MARK: - Post-processor

enum {class_name}PostProcessor {{

    /// Output tensor names produced by the model.
{output_name_constants}

    // MARK: - Public API

    /// Classify raw model outputs and return top-K predictions.
    ///
    /// - Parameters:
    ///   - outputs: Dictionary of output name -> MLMultiArray from the predictor.
    ///   - topK: Number of top predictions to return (default 5).
    ///   - labels: Optional array of class label strings indexed by class index.
    /// - Returns: An array of `ClassificationResult` sorted by confidence (descending).
    static func classify(
        outputs: [String: MLMultiArray],
        topK: Int = 5,
        labels: [String]? = nil
    ) -> [ClassificationResult] {{
        guard let tensor = outputs.values.first else {{
            return []
        }}

        let count = tensorElementCount(tensor)
        guard count > 0 else {{ return [] }}

        let ptr = tensor.dataPointer.assumingMemoryBound(to: Float.self)

        // Extract raw values
        var values = [Float](repeating: 0, count: count)
        for i in 0..<count {{
            values[i] = ptr[i]
        }}

        // Apply softmax if values are not already probabilities.
        // Heuristic: if any value is negative or any value > 1, treat as logits.
        let needsSoftmax = values.contains(where: {{ $0 < 0 || $0 > 1.0 }})
        if needsSoftmax {{
            values = softmax(values)
        }}

        // Build indexed pairs and sort descending by score
        let indexed = values.enumerated().map {{ (index: $0.offset, score: $0.element) }}
        let sorted = indexed.sorted {{ $0.score > $1.score }}

        // Take top-K
        let k = min(topK, sorted.count)
        return sorted.prefix(k).map {{ item in
            ClassificationResult(
                classIndex: item.index,
                confidence: item.score,
                label: labels.flatMap {{ $0.indices.contains(item.index) ? $0[item.index] : nil }}
            )
        }}
    }}

    // MARK: - Helpers

    /// Compute the total number of elements in an MLMultiArray.
    private static func tensorElementCount(_ tensor: MLMultiArray) -> Int {{
        var count = 1
        for i in 0..<tensor.shape.count {{
            let dim = tensor.shape[i].intValue
            // Skip batch dimensions of size 1 at the front
            if i == 0 && dim == 1 && tensor.shape.count > 1 {{
                continue
            }}
            count *= dim
        }}
        return count
    }}

    /// Apply the softmax function to an array of logits.
    private static func softmax(_ logits: [Float]) -> [Float] {{
        let maxVal = logits.max() ?? 0
        let exps = logits.map {{ exp($0 - maxVal) }}
        let sumExps = exps.reduce(0, +)
        guard sumExps > 0 else {{ return logits }}
        return exps.map {{ $0 / sumExps }}
    }}
}}
"""


